#!/usr/bin/env node
// Build .brain/catalogue.tsv (one line per vault file) and .brain/topic-map.tsv
// (wiki section heading → owner page). Deterministic — no LLM.
//
//   node .brain/scripts/build-catalogue.mjs
//
// Re-run any time; manual lines in topic-map.tsv under "# MANUAL" are preserved.

import fs from 'node:fs';
import path from 'node:path';
import { VAULT, CATALOGUE, TOPIC_MAP, parseFrontmatter, tokenize, fold } from './lib.mjs';

const SCAN_DIRS = ['wiki', 'sources', 'projects', 'briefs'];

function* walk(dir) {
  for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
    if (e.name.startsWith('.') || e.name.startsWith('_')) continue;
    const p = path.join(dir, e.name);
    if (e.isDirectory()) yield* walk(p);
    else if (e.name.endsWith('.md')) yield p;
  }
}

function axisOf(rel, fm) {
  if (rel.startsWith('wiki/method/meta/')) return 'wiki-meta';
  if (rel.startsWith('wiki/method/')) return 'wiki-method';
  if (rel.startsWith('wiki/products/')) return 'wiki-product';
  if (rel.startsWith('wiki/')) return 'wiki';
  if (rel.startsWith('sources/')) return `source-${fm.type || 'clip'}`;
  if (rel.startsWith('projects/')) return 'project';
  if (rel.startsWith('briefs/')) return 'brief';
  return 'other';
}

function firstHeading(body) {
  const m = body.match(/^#\s+(.+)$/m);
  return m ? m[1].trim() : '';
}

function firstBodyLine(body) {
  for (const line of body.split('\n')) {
    const t = line.trim();
    if (!t || t.startsWith('#') || t.startsWith('---') || t.startsWith('![') || t.startsWith('>')) continue;
    return t;
  }
  return '';
}

function clean(s, max = 220) {
  const t = s.replace(/\t/g, ' ').replace(/\s+/g, ' ').trim();
  return t.length > max ? t.slice(0, max - 1) + '…' : t;
}

// Teasers from wiki/index.md: "- [[slug]] — description"
function indexTeasers() {
  const map = {};
  const idx = path.join(VAULT, 'wiki/index.md');
  if (!fs.existsSync(idx)) return map;
  for (const line of fs.readFileSync(idx, 'utf8').split('\n')) {
    const m = line.match(/\[\[([^\]|]+)(?:\|[^\]]*)?\]\]\s*[—–-]\s*(.+)$/);
    if (m) map[m[1].trim()] = clean(m[2], 300);
  }
  return map;
}

const teasers = indexTeasers();
const rows = [];
const topicRows = [];

for (const dir of SCAN_DIRS) {
  const abs = path.join(VAULT, dir);
  if (!fs.existsSync(abs)) continue;
  for (const file of walk(abs)) {
    const rel = path.relative(VAULT, file);
    const raw = fs.readFileSync(file, 'utf8');
    const { fm, body } = parseFrontmatter(raw);
    const axis = axisOf(rel, fm);
    const slug = path.basename(rel, '.md');

    const title = fm.title || firstHeading(body) || slug;
    const date = fm.updated || fm.date || '';

    let desc = teasers[slug] || fm.note || '';
    if (!desc) {
      if (axis.startsWith('source-')) desc = fm.title ? firstBodyLine(body) : '';
      else desc = firstBodyLine(body);
    }

    // keywords: section headings + product/tags frontmatter + folder hints
    const kw = new Set();
    for (const m of body.matchAll(/^#{2,4}\s+(.+)$/gm)) {
      for (const t of tokenize(m[1])) kw.add(t);
    }
    if (fm.product) for (const t of tokenize(String(fm.product))) kw.add(t);
    if (fm.tags) for (const tag of [].concat(fm.tags)) for (const t of tokenize(tag)) kw.add(t);
    for (const t of tokenize(slug)) kw.add(t);
    const keywords = [...kw].slice(0, 40).join(',');

    rows.push([rel, axis, clean(title, 160), date, clean(desc), keywords].join('\t'));

    // topic map: wiki pages only — every ##/### heading owns its page
    if (axis.startsWith('wiki')) {
      for (const m of body.matchAll(/^#{2,3}\s+(.+)$/gm)) {
        const phrase = clean(fold(m[1]).replace(/[^a-z0-9\s-]/g, ' '), 120);
        if (tokenize(phrase).length >= 1) topicRows.push([phrase, rel].join('\t'));
      }
    }
  }
}

rows.sort();
fs.writeFileSync(
  CATALOGUE,
  '# path\taxis\ttitle\tdate\tdesc\tkeywords  — generated by build-catalogue.mjs, do not hand-edit\n' +
    rows.join('\n') + '\n'
);

// preserve manual section of topic-map
let manual = '';
if (fs.existsSync(TOPIC_MAP)) {
  const cur = fs.readFileSync(TOPIC_MAP, 'utf8');
  const i = cur.indexOf('# MANUAL');
  if (i !== -1) manual = cur.slice(i).trimEnd() + '\n';
}
if (!manual) manual = '# MANUAL — thêm dòng "phrase<TAB>path" dưới đây, giữ nguyên khi rebuild\n';

fs.writeFileSync(
  TOPIC_MAP,
  '# phrase\tpath — generated from wiki headings by build-catalogue.mjs\n' +
    topicRows.join('\n') + '\n\n' + manual
);

console.log(`catalogue: ${rows.length} entries → ${path.relative(VAULT, CATALOGUE)}`);
console.log(`topic-map: ${topicRows.length} phrases → ${path.relative(VAULT, TOPIC_MAP)}`);
const noDesc = rows.filter((r) => !r.split('\t')[4]).length;
console.log(`entries missing desc: ${noDesc}`);
